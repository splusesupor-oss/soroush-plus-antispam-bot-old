from pathlib import Path

p = Path("main.py")
s = p.read_text(encoding="utf-8")

# اضافه کردن چیستان بعد از جرعت حقیقت
old = '''"جرعت → یک جرعت تصادفی دریافت کنید\\n"
                      "حقیقت → یک سوال حقیقت تصادفی دریافت کنید\\n\\n"'''

new = '''"جرعت → یک جرعت تصادفی دریافت کنید\\n"
                      "حقیقت → یک سوال حقیقت تصادفی دریافت کنید\\n"
                      "چیستان → یک معمای تصادفی دریافت کنید و ۵۰ ثانیه فرصت جواب دارید 🧩\\n\\n"'''

if old in s:
    s = s.replace(old, new, 1)
    print("✅ چیستان اضافه شد")
else:
    print("⚠️ بخش جرعت/حقیقت پیدا نشد")

# اضافه کردن لغو و فعال کلمات ممنوعه بعد از فیلترها
old2 = '''"/فیلترها  ← نمایش لیست فیلترهای گروه\\n\\n"'''

new2 = '''"/فیلترها  ← نمایش لیست فیلترهای گروه\\n"
                        "لغو کلمات ممنوعه ← خاموش کردن فیلتر کلمات\\n"
                        "فعال کلمات ممنوعه ← روشن کردن فیلتر کلمات\\n\\n"'''

if old2 in s:
    s = s.replace(old2, new2, 1)
    print("✅ فعال/لغو کلمات اضافه شد")
else:
    print("⚠️ بخش فیلتر پیدا نشد")

p.write_text(s, encoding="utf-8")
print("✅ تمام شد")
