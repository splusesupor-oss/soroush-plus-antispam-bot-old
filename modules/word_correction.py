_active = {}
_remaining = {}
_tokens = count(1)

def start(chat_id):
    remaining = _remaining.get(chat_id)
    if not remaining:
        remaining = list(range(len(WORDS)))
        _remaining[chat_id] = remaining
    index = random.choice(remaining)
    remaining.remove(index)
    wrong, correct = WORDS[index]
    token = next(_tokens)
    _active[chat_id] = {'wrong': wrong, 'correct': correct, 'token': token}
    return dict(_active[chat_id])

def answer(chat_id, text):
    data = _active.get(chat_id)
    if not data:
        return None
    if str(text).strip() == data['correct']:
        _active.pop(chat_id, None)
        return True
    return False

def get(chat_id):
    return dict(_active[chat_id]) if chat_id in _active else None

def clear(chat_id, token=None):
    data = _active.get(chat_id)
    if data and (token is None or data['token'] == token):
        _active.pop(chat_id, None)
        return True
    return False
