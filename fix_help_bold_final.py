from pathlib import Path

p = Path("main.py")
s = p.read_text(encoding="utf-8")

# اصلاح بخش چیستان
s = s.replace(
'''                      "حقیقت → یک سوال حقیقت تصادفی دریافت کنید\n"
                      "چیستان → یک معمای تصادفی دریافت کنید 🧩\n"
                      "۵۰ ثانیه فرصت دارید\n\n"''',
'''                      "حقیقت → یک سوال حقیقت تصادفی دریافت کنید\n\n"
                      "🧩 چیستان:\n"
                      "یک معمای تصادفی دریافت کنید\n"
                      "⏳ ۵۰ ثانیه فرصت دارید\n\n"'''
)

# اضافه کردن عنوان ها به بخش bold
marker = '''"🎯 بازی جرعت حقیقت:",'''

if marker in s and '"🧩 چیستان:"' not in s[s.find(marker):s.find(marker)+500]:
    s = s.replace(
        marker,
        '''"🎯 بازی جرعت حقیقت:",
                        "🧩 چیستان:",
                        "🚫 خاموش کردن فیلتر کلمات:",
                        "✅ روشن کردن فیلتر کلمات:",''',
        1
    )
    print("✅ عنوان‌ها به Bold اضافه شدند")
else:
    print("⚠️ لیست Bold قبلا تغییر کرده یا پیدا نشد")

p.write_text(s, encoding="utf-8")
print("✅ تمام شد")
