from pathlib import Path

p = Path("main.py")
s = p.read_text(encoding="utf-8")

old = '''                      "جرعت → یک جرعت تصادفی دریافت کنید\\n"
                      "حقیقت → یک سوال حقیقت تصادفی دریافت کنید\\n"
                      "چیستان → یک معمای تصادفی دریافت کنید 🧩\\n"
                      "۵۰ ثانیه فرصت دارید\\n\\n"'''

new = '''                      "جرعت → یک جرعت تصادفی دریافت کنید\\n"
                      "حقیقت → یک سوال حقیقت تصادفی دریافت کنید\\n\\n"
                      "🧩 چیستان\\n"
                      "یک معمای تصادفی دریافت کنید\\n"
                      "⏳ ۵۰ ثانیه فرصت دارید\\n\\n"'''

if old in s:
    s = s.replace(old, new, 1)
    print("✅ چیستان اصلاح شد")
else:
    print("❌ بخش پیدا نشد")

p.write_text(s, encoding="utf-8")
